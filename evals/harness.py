"""Offline eval harness: benchmark every planner against the A* optimum.

Run: ``python -m evals.harness`` (writes ``evals/RESULTS.md``).

It sweeps all five planners over deterministic, seeded instances of all three
domains with the offline stub proposer, then repeats the two LLM-guided planners
with the *random* proposer as an ablation. Because the optimum is computed for
every instance, the optimality ratios are anchored to ground truth -- not to
another heuristic's opinion. The whole run uses no API keys and no downloads.
"""

from __future__ import annotations

import json
from pathlib import Path

from evals.metrics import aggregate
from pathwai.config import Settings
from pathwai.domains import make_domain
from pathwai.pipeline import solve_instance
from pathwai.planners import PLANNERS

DOMAINS_EVAL = ("gridworld", "blocksworld", "delivery")
N_SEEDS = 50
RESULTS_PATH = Path(__file__).parent / "RESULTS.md"


def _run(domain_name: str, planner: str, backend: str) -> dict:
    results = []
    for seed in range(N_SEEDS):
        cfg = Settings.from_env()
        cfg.domain = domain_name
        cfg.seed = seed
        cfg.proposer_backend = backend
        cfg.planner = planner
        domain = make_domain(domain_name, seed)
        results.append(solve_instance(domain, cfg, planner))
    return aggregate(results)


def _pool(per_domain: dict, planner: str, key: str) -> float:
    """Average a metric for ``planner`` across the three domains (equal weight)."""
    vals = [per_domain[d][planner][key] for d in DOMAINS_EVAL]
    return round(sum(vals) / len(vals), 3)


def run_eval() -> dict:
    # Main result: all planners, offline stub proposer.
    per_domain = {
        d: {p: _run(d, p, "stub") for p in PLANNERS} for d in DOMAINS_EVAL
    }
    overall = {
        p: {k: _pool(per_domain, p, k) for k in ("solve_rate", "optimality_ratio", "expansions")}
        for p in PLANNERS
    }

    # Ablation: swap the stub for the uninformative random proposer.
    ablation_planners = ("llm", "llm_search")
    ablation_domain = {
        d: {p: _run(d, p, "random") for p in ablation_planners} for d in DOMAINS_EVAL
    }
    ablation = {
        p: {
            k: round(sum(ablation_domain[d][p][k] for d in DOMAINS_EVAL) / len(DOMAINS_EVAL), 3)
            for k in ("solve_rate", "optimality_ratio", "expansions")
        }
        for p in ablation_planners
    }

    return {
        "config": {
            "domains": list(DOMAINS_EVAL),
            "seeds_per_domain": N_SEEDS,
            "total_instances": N_SEEDS * len(DOMAINS_EVAL),
            "proposer": "stub (offline) + random (ablation)",
        },
        "per_domain": per_domain,
        "overall": overall,
        "ablation": ablation,
        # Flat scalars consumed by the gate.
        "optimal_solve_rate": overall["optimal"]["solve_rate"],
        "optimal_ratio": overall["optimal"]["optimality_ratio"],
        "llm_solve_rate": overall["llm"]["solve_rate"],
        "llm_search_solve_rate": overall["llm_search"]["solve_rate"],
        "llm_search_ratio": overall["llm_search"]["optimality_ratio"],
        "llm_search_expansions": overall["llm_search"]["expansions"],
        "uniform_expansions": overall["uniform"]["expansions"],
        "ablation_llm_solve_rate": ablation["llm"]["solve_rate"],
        "ablation_llm_search_expansions": ablation["llm_search"]["expansions"],
    }


def _render_markdown(res: dict) -> str:
    c = res["config"]
    ov = res["overall"]
    ab = res["ablation"]
    lines = [
        "# PathwAI - Offline Evaluation Results",
        "",
        "Generated by `python -m evals.harness` over "
        f"{c['total_instances']} deterministic instances "
        f"({c['seeds_per_domain']} seeds x {len(c['domains'])} domains), no API keys, "
        "no downloads. Every instance is scored against its **A\\* optimum**.",
        "",
        "## Headline: all planners, offline stub proposer (pooled across domains)",
        "",
        "| Planner | Solve rate | Optimality ratio | Avg expansions |",
        "|---|---:|---:|---:|",
    ]
    labels = {
        "optimal": "optimal (A\\*) - ground truth",
        "uniform": "uniform-cost (no heuristic)",
        "greedy": "greedy best-first",
        "llm": "llm (open-loop, no backtrack)",
        "llm_search": "**llm + search + verifier**",
    }
    for p in PLANNERS:
        m = ov[p]
        ratio = "1.000" if p == "optimal" else f"{m['optimality_ratio']:.3f}"
        bold = "**" if p == "llm_search" else ""
        lines.append(
            f"| {labels[p]} | {bold}{m['solve_rate']:.2f}{bold} | {bold}{ratio}{bold} | "
            f"{bold}{m['expansions']:.1f}{bold} |"
        )
    lines += [
        "",
        "Two effects, cleanly separated:",
        "",
        f"- **Search + verifier buys correctness.** Open-loop `llm` solves only "
        f"{ov['llm']['solve_rate']:.0%} at optimality ratio {ov['llm']['optimality_ratio']:.2f}; "
        f"adding search + verification lifts it to {ov['llm_search']['solve_rate']:.0%} at "
        f"{ov['llm_search']['optimality_ratio']:.3f} - essentially optimal.",
        f"- **Guidance buys efficiency.** `llm + search` reaches that optimum while expanding "
        f"{ov['llm_search']['expansions']:.0f} nodes vs. uniform-cost search's "
        f"{ov['uniform']['expansions']:.0f} - the LLM heuristic focuses the frontier.",
        "",
        "## Ablation (the null test): replace the stub with a *random* proposer",
        "",
        "| Planner | Solve rate | Optimality ratio | Avg expansions |",
        "|---|---:|---:|---:|",
        f"| llm (open-loop), random guidance | {ab['llm']['solve_rate']:.2f} | "
        f"{ab['llm']['optimality_ratio']:.3f} | {ab['llm']['expansions']:.1f} |",
        f"| llm + search, random guidance | {ab['llm_search']['solve_rate']:.2f} | "
        f"{ab['llm_search']['optimality_ratio']:.3f} | {ab['llm_search']['expansions']:.1f} |",
        "",
        "Strip the guidance and the two effects dissociate exactly as the theory predicts: "
        f"the open-loop agent **collapses** ({ab['llm']['solve_rate']:.0%} solved), while the "
        "search+verifier agent still solves everything but its expansions balloon from "
        f"{ov['llm_search']['expansions']:.0f} to {ab['llm_search']['expansions']:.0f} - "
        "drifting back toward uninformed search. Correctness comes from the verifier; "
        "efficiency comes from the guidance.",
        "",
        "## Per-domain breakdown (stub proposer)",
        "",
    ]
    for d in DOMAINS_EVAL:
        lines += [
            f"### {d}",
            "",
            "| Planner | Solve rate | Optimality ratio | Avg expansions |",
            "|---|---:|---:|---:|",
        ]
        for p in PLANNERS:
            m = res["per_domain"][d][p]
            ratio = "1.000" if p == "optimal" else f"{m['optimality_ratio']:.3f}"
            lines.append(
                f"| {p} | {m['solve_rate']:.2f} | {ratio} | {m['expansions']:.1f} |"
            )
        lines.append("")
    return "\n".join(lines)


def main() -> int:
    res = run_eval()
    RESULTS_PATH.write_text(_render_markdown(res), encoding="utf-8")
    # ASCII-only console output (Windows console is cp1252).
    print("PathwAI eval complete. Wrote", RESULTS_PATH.name)
    summary = {
        "overall": res["overall"],
        "ablation": res["ablation"],
    }
    print(json.dumps(summary, indent=2))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
